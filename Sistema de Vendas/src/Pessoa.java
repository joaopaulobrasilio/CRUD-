package SistemaVendas;

public class Pessoa {
   
protected String nome; 
protected String sexo;
protected int CPF;
public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public String getSexo() {
	return sexo;
}
public void setSexo(String sexo) {
	this.sexo = sexo;
}
public int getCPF() {
	return CPF;
}
public void setCPF(int cPF) {
	CPF = cPF;
}

}
